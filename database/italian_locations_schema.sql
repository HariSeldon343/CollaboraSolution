-- ============================================
-- TABLE DEFINITIONS
-- ============================================

-- Italian Provinces (107 provinces)
CREATE TABLE italian_provinces (
    -- Primary key
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,

    -- Province data
    code VARCHAR(2) NOT NULL COMMENT 'Two-letter province code (e.g., RM, MI)',
    name VARCHAR(100) NOT NULL COMMENT 'Full province name',
    region VARCHAR(50) NOT NULL COMMENT 'Italian region name',

    -- Audit fields (no tenant_id - system data)
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    -- Constraints
    PRIMARY KEY (id),
    UNIQUE KEY uk_province_code (code)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
COMMENT='Italian provinces - system reference data (no multi-tenancy)';

-- Italian Municipalities (7900+ comuni)
CREATE TABLE italian_municipalities (
    -- Primary key
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,

    -- Municipality data
    istat_code VARCHAR(6) NOT NULL COMMENT 'Official ISTAT code',
    name VARCHAR(100) NOT NULL COMMENT 'Municipality name',
    province_code VARCHAR(2) NOT NULL COMMENT 'Province code reference',

    -- Optional data for future enhancements
    cadastral_code VARCHAR(4) DEFAULT NULL COMMENT 'Catasto code (Codice Belfiore)',
    postal_code_prefix VARCHAR(5) DEFAULT NULL COMMENT 'CAP prefix',

    -- Audit fields (no tenant_id - system data)
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    -- Constraints
    PRIMARY KEY (id),
    UNIQUE KEY uk_istat_code (istat_code),
    FOREIGN KEY (province_code) REFERENCES italian_provinces(code) ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
COMMENT='Italian municipalities - system reference data (no multi-tenancy)';

-- ============================================
-- INDEXES
-- ============================================

-- Province search optimization
CREATE INDEX idx_province_name ON italian_provinces(name);
CREATE INDEX idx_province_region ON italian_provinces(region);

-- Municipality search and validation optimization
CREATE INDEX idx_municipality_name ON italian_municipalities(name);
CREATE INDEX idx_municipality_province ON italian_municipalities(province_code);
CREATE INDEX idx_municipality_postal ON italian_municipalities(postal_code_prefix);

-- Composite index for validation queries (province + municipality)
CREATE INDEX idx_validation_lookup ON italian_municipalities(name, province_code);

